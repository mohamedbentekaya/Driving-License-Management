        public static DataTable GetAllPeople()
        {
            DataTable dataTable = new DataTable();

            SqlConnection connection = new SqlConnection(ClsDataAccessSettings.ConnectionString);

            string query = "select PersonID, NationalNo, FirstName, LastName, CASE        WHEN Gendor = 0 THEN 'Male'        WHEN Gendor = 1 THEN 'Female'       ELSE 'Unknown'   END AS Gendor, DateOfBirth,(select CountryName from Countries where CountryID=NationalityCountryID) as Nationality, Phone, Email from People;";

            SqlCommand command = new SqlCommand(query, connection);

            try
            {
                connection.Open();
                SqlDataReader reader = command.ExecuteReader();
                if (reader.HasRows)
                {
                    dataTable.Load(reader);
                }
                reader.Close();
            }
            catch (Exception ex)
            {
                //Console.WriteLine("Error"+ex.Message);
            }
            finally
            {
                connection.Close();
            }
            return dataTable;
        }

